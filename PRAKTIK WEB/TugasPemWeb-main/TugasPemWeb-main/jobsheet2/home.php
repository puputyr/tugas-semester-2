            <label>Program yang ditujukan untuk menjamin agar peserta menerima uang tunai apabila memasuki masa pensiun,mengalami cacat total tetap, atau meninggal dunia. Jaminan hari tua diselenggaralan berdasarkan prinsip asuransi sosial atau tabungan wajib. Manfaat jaminan hari tua berupa uang tunai yang berasal dari akumulasi iuran dan hasil pengembangannya.</label>
