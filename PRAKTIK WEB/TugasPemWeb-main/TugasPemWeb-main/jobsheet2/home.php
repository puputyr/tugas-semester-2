            <label>Program yang memberikan perlindungan dalam rangka menanggulangi hilangnya sebagian atau seluruh penghasilan yang diakibatkan oleh adanya risiko-risiko sosial seperti kematian atau cacat karena <br> kecelakaan kerja baik fisik maupun mental. Jaminan kecelakaan kerja diselenggarakan berdasarkan prinsip asuransi sosial.<br> Prinsip asuransi sosial meliputi: <br> 1. kegotong-royongan antara yang kaya dan miskin, yang sehat dan sakit, yang tua dan muda, yang berisiko tinggi dan rendah; <br> 2. kepesertaan bersifat wajib dan tidak selektif; <br> 3. iuran berdasarkan presentase upah/penghasilan; <br> bersifat nirlaba.<br> Peserta yang mengalami kecelakaan kerja berhak mendapatkan manfaat berupa pelayanan kesehatan sesuai dengan kebutuhan medisnya dan mendapatkan manfaat berupa uang tunai apabila terjadi cacat total tetap atau meninggal dunia.</label>
